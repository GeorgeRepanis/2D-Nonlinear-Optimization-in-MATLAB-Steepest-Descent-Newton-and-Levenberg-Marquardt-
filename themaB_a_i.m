%% Σύγκλιση Steepest Descent για x0 = (0,0), gamma = 0.2
% Επίδειξη "μη σύγκλισης" (ο αλγόριθμος μένει κολλημένος στο (0,0))

clear; clc; close all;

% Συνάρτηση f (για πληρότητα)
f = @(x) x(1).^3 .* exp(-x(1).^2 - x(2).^4);

% Αρχικό σημείο
x0 = [0; 0];
f0 = f(x0);   % αυτό είναι 0

% Τεχνητός αριθμός "βημάτων" απλώς για να φανεί ως επίπεδη γραμμή
K = 20;                      % π.χ. 20 επαναλήψεις
k = 0:K;                     % άξονας επαναλήψεων
Fhist = f0 * ones(size(k));  % f(x_k) = 0 για όλα τα k

% Γράφημα
figure;
plot(k, Fhist, 'o-','LineWidth',1.2);
xlabel('Επαναλήψεις k');
ylabel('f(x_k)');
title('f(x_k) για Steepest Descent με x_0 = (0,0) και \gamma = 0.2');
grid on;
ylim([-0.5 0.5]);   % για να φαίνεται καθαρά η επίπεδη γραμμή γύρω από το 0
